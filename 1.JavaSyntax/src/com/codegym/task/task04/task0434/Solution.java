/* 
Multiplication table

*/

import java.io.*;

public class Solution {
    public static void main(String[] args) throws Exception {
        int count = 1;
        while(count<11){
            System.out.println(count*1 + " " + count*2 + " " + count*3 + " " + count*4 + " " + count*5 + " " + count*6 + " " + count*7 + " " + count*8 + " " + count*9 + " " + count*10);
            count++;
        }

    }
}
